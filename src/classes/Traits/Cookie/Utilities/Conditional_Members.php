/**
 * Utility members.
 *
 * @since 2021-12-15
 *
 * @see   U\Cookie
 */
trait Conditional_Members {
	/**
	 * Is a valid cookie name?
	 *
	 * @since 2022-04-25
	 *
	 * @param string $name Cookie name.
	 *
	 * @returns bool `true` if valid cookie name.
	 */
	public static function is_valid_name( string $name ) : bool {
		return $name && preg_match( '/^[a-z0-9_\-]+$/ui', $name )
			&& ! preg_match( '/^(?:domain|path|expires|max-age|samesite|secure|httponly)$/ui', $name );
	}
}
