/**
 * Utility members.
 *
 * @since 2021-12-15
 *
 * @see   U\Str
 */
trait Is_UTF8_Members {
	/**
	 * Is valid UTF-8?
	 *
	 * @since 2021-12-26
	 *
	 * @param string $str Input string.
	 *
	 * @return bool True if valid UTF-8.
	 */
	public static function is_utf8( string $str ) : bool {
		if ( ! isset( $str[ 0 ] ) ) {
			return true; // Nothing to do.
		}
		return (bool) preg_match( '/^./us', $str );
	}
}
